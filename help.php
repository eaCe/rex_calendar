<h2>Redaxo Kalender</h2>
<p>
    Dieses Addon stellt Kalender im Backend zur verfügung.
</p>
<p>
    Ein Eintrag kann im Feld des Tages direkt über das +-Zeichen hinzugefügt werden.
    <br>
    In den Einstellungen können Kategorien kommasepariert eingegeben werden. Diese stehen dann beim hinzufügen als select zur verfügung.
    <br>
    Weiter kann man entscheiden ob man den Markitup Editor oder nur eine Textarea nutzt.
    Sollte man Markitup nutzen, muss hierzu in diesem ein Profil mit dem Namen "<strong>calendar</strong>" angelegt werden.
</p>
